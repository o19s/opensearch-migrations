#!/usr/bin/env python3
"""
Promptfoo exec provider — Tier 3: LLM + skill context, NO pre-converted mapping.

Feeds Claude the assembled skill context as a system prompt and the user's
raw Solr schema — but does NOT provide a pre-converted OpenSearch mapping.
The LLM must derive or reason about the mapping from skill guidance alone.

This is the hardest pure-skill test: can the skill content teach the LLM
enough to produce a structurally complete report without a conversion crutch?

Usage in promptfooconfig.yaml:
  providers:
    - "exec:python3 skill_llm_no_mapping_provider.py"
"""

import json
import os
import shutil
import subprocess
import sys

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
SYSTEM_PROMPT_FILE = os.path.join(SCRIPT_DIR, "system-prompt-assembled.txt")


def main():
    prompt = sys.argv[1] if len(sys.argv) > 1 else os.environ.get("PROMPT", "")
    if not prompt:
        print(json.dumps({"output": "ERROR: No prompt received"}))
        return

    claude_path = shutil.which("claude")
    if not claude_path:
        print(json.dumps({"output": "SKIPPED: claude CLI not found"}))
        return

    if not os.path.isfile(SYSTEM_PROMPT_FILE):
        print(json.dumps({"output": "ERROR: system-prompt-assembled.txt not found. Run run_eval.sh to generate it."}))
        return

    with open(SYSTEM_PROMPT_FILE) as f:
        system_prompt = f.read()

    try:
        result = subprocess.run(
            [
                claude_path, "-p",
                "--append-system-prompt", system_prompt,
                "--tools", "",
                "--output-format", "text",
                "--max-turns", "1",
                "--dangerously-skip-permissions",
                prompt,
            ],
            capture_output=True,
            text=True,
            timeout=120,
        )
        output = result.stdout.strip()
        if not output and result.stderr:
            output = f"ERROR: {result.stderr.strip()}"
    except subprocess.TimeoutExpired:
        output = "ERROR: claude CLI timed out after 120s"
    except Exception as e:
        output = f"ERROR: {e}"

    # Save report artifact
    artifact_dir = os.environ.get("REPORT_ARTIFACT_DIR")
    if artifact_dir and not output.startswith(("ERROR", "SKIPPED")):
        os.makedirs(artifact_dir, exist_ok=True)
        path = os.path.join(artifact_dir, "migration-report-t3-skill-no-mapping.md")
        with open(path, "w") as f:
            f.write("<!-- Generated by skill_llm_no_mapping_provider.py (T3: LLM + skill, no OS mapping) -->\n\n")
            f.write(output)

    print(json.dumps({"output": output}))


if __name__ == "__main__":
    main()
