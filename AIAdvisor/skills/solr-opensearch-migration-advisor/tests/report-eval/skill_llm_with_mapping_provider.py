#!/usr/bin/env python3
"""
Promptfoo exec provider — Tier 2: LLM + skill context + pre-converted mapping.

Feeds Claude the assembled skill context as a system prompt, plus the user's
Solr schema and its pre-converted OpenSearch mapping. No MCP tools — the LLM
must produce a migration report purely from skill guidance.

Usage in promptfooconfig.yaml:
  providers:
    - "exec:python3 skill_llm_with_mapping_provider.py"
"""

import json
import os
import shutil
import subprocess
import sys

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
SCRIPTS_DIR = os.path.abspath(os.path.join(SCRIPT_DIR, "..", "..", "scripts"))
SYSTEM_PROMPT_FILE = os.path.join(SCRIPT_DIR, "system-prompt-assembled.txt")


def _extract_schema_xml(prompt: str) -> str:
    """Extract <schema ...>...</schema> from the prompt text."""
    start = prompt.find("<schema")
    end = prompt.find("</schema>")
    if start == -1 or end == -1:
        return ""
    return prompt[start : end + len("</schema>")]


def _convert_schema(schema_xml: str) -> str:
    """Use the deterministic schema converter to produce an OpenSearch mapping."""
    sys.path.insert(0, SCRIPTS_DIR)
    from schema_converter import SchemaConverter

    converter = SchemaConverter()
    mapping = converter.convert_xml(schema_xml)
    return json.dumps(mapping, indent=2)


def main():
    prompt = sys.argv[1] if len(sys.argv) > 1 else os.environ.get("PROMPT", "")
    if not prompt:
        print(json.dumps({"output": "ERROR: No prompt received"}))
        return

    claude_path = shutil.which("claude")
    if not claude_path:
        print(json.dumps({"output": "SKIPPED: claude CLI not found"}))
        return

    if not os.path.isfile(SYSTEM_PROMPT_FILE):
        print(json.dumps({"output": "ERROR: system-prompt-assembled.txt not found. Run run_eval.sh to generate it."}))
        return

    with open(SYSTEM_PROMPT_FILE) as f:
        system_prompt = f.read()

    # Pre-convert the schema and append the mapping to the user prompt
    schema_xml = _extract_schema_xml(prompt)
    if schema_xml:
        mapping_json = _convert_schema(schema_xml)
        augmented_prompt = (
            prompt
            + "\n\nHere is the pre-converted OpenSearch mapping:\n```json\n"
            + mapping_json
            + "\n```"
        )
    else:
        augmented_prompt = prompt

    try:
        result = subprocess.run(
            [
                claude_path, "-p",
                "--append-system-prompt", system_prompt,
                "--tools", "",
                "--output-format", "text",
                "--max-turns", "1",
                "--dangerously-skip-permissions",
                augmented_prompt,
            ],
            capture_output=True,
            text=True,
            timeout=120,
        )
        output = result.stdout.strip()
        if not output and result.stderr:
            output = f"ERROR: {result.stderr.strip()}"
    except subprocess.TimeoutExpired:
        output = "ERROR: claude CLI timed out after 120s"
    except Exception as e:
        output = f"ERROR: {e}"

    # Save report artifact
    artifact_dir = os.environ.get("REPORT_ARTIFACT_DIR")
    if artifact_dir and not output.startswith(("ERROR", "SKIPPED")):
        os.makedirs(artifact_dir, exist_ok=True)
        path = os.path.join(artifact_dir, "migration-report-t2-skill-with-mapping.md")
        with open(path, "w") as f:
            f.write("<!-- Generated by skill_llm_with_mapping_provider.py (T2: LLM + skill + OS mapping) -->\n\n")
            f.write(output)

    print(json.dumps({"output": output}))


if __name__ == "__main__":
    main()
